const kanjiReadingPairs = [...rawKanjiReadingPairs].sort((a, b) => b[0].length - a[0].length);

export function getExampleReading(example: ExampleSentence): string {
  const explicitReading = example.reading?.trim();

  if (explicitReading) {
    return explicitReading;
  }

  return toKanaReading(example.ja);
}

export function toKanaReading(text: string): string {
  return kanjiReadingPairs.reduce(
    (reading, [kanji, kana]) => reading.split(kanji).join(kana),
    text
  );
}

export function hasKanji(text: string): boolean {
  return /[\u3400-\u9fff]/.test(text);
}
